const mongoose = require('mongoose');

const { Schema } = mongoose;

const OwnerSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    age: {
      type: Number,
      required: true,
    },
    email: {
      type: String,
      lowercase: true,
      trim: true,
      unique: true,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
    updatedAt: {
      type: Date,
      default: Date.now,
    },
  },
  {
    collection: 'owners',
  },
);

const User = mongoose.model('Owner', OwnerSchema);

module.exports = User;
